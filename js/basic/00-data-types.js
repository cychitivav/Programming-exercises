// Primitive Data Types
console.log("Primitive Data Types\n--------------------")
console.log(typeof 40) // number
console.log(typeof "Hello") // string
console.log(typeof true) // boolean
console.log(typeof null) // object (bug in JS, should be null)
console.log(typeof undefined) // undefined

// Non-Primitive Data Types
console.log("\nNon-Primitive Data Types\n------------------------")
console.log(typeof {name: "Cristian"}) // object
console.log(typeof [1, 2, 3]) // object (array)